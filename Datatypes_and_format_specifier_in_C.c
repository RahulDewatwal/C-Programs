#include<stdio.h>
int main()
{
    int a = 1;
    float b = 5.2;
    double c = 5.3;
    char z = 'a';
    printf ("value of a(int)=%d", a);
    printf ("\nvalue of b(float)=%f", b);
    printf ("\nvalue of c(double)=%.12f", c);
    printf ("\nvalue of z(char)=%c",z);
    
    return 0;
}
